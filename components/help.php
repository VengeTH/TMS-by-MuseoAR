<div class="help-container" style="padding: 2rem; max-width: 960px; margin: 0 auto;">
    <h2 style="margin-bottom: 1rem;">Help &amp; Guide</h2>

    <section style="margin-bottom: 1.5rem;">
        <h3>Getting Started</h3>
        <p>
            Use the <strong>Dashboard</strong> tab to see a quick overview of your tasks and progress,
            and the <strong>My Task</strong> tab to manage all of your tasks in detail.
        </p>
    </section>

    <section style="margin-bottom: 1.5rem;">
        <h3>Managing Tasks</h3>
        <ul>
            <li><strong>Create a task</strong>: Click the New Task button, then provide a title, optional details, a finish date, and priority.</li>
            <li><strong>Complete tasks</strong>: Use the checkbox beside a task in the My Task tab to mark it as done.</li>
            <li><strong>Parent tasks and subtasks</strong>: Some tasks can have subtasks. Use the arrow icon to expand or collapse subtasks.</li>
            <li><strong>Delete tasks</strong>: Select one or more tasks, then use the Delete action to remove them.</li>
        </ul>
    </section>

    <section style="margin-bottom: 1.5rem;">
        <h3>AI Features</h3>
        <ul>
            <li><strong>AI Task Breakdown</strong>: From a task, trigger the AI breakdown to automatically generate smaller subtasks based on the task title.</li>
            <li><strong>AI Weekly Planner</strong>: Open the AI Weekly Planner from the sidebar to let the system propose a weekly schedule for your selected tasks.</li>
            <li><strong>Daily limits</strong>: AI usage is limited per user per day; if you reach the limit, you can try again the next day.</li>
        </ul>
    </section>

    <section style="margin-bottom: 1.5rem;">
        <h3>Account &amp; Security</h3>
        <ul>
            <li><strong>Change password</strong>: Use the Settings menu and the password change flow to update your password.</li>
            <li><strong>Change email</strong>: Use the email change flow; a verification code is sent to your new email address.</li>
            <li><strong>Profile picture</strong>: Upload a profile picture from the Settings tab if available.</li>
        </ul>
    </section>

    <section>
        <h3>Need More Help?</h3>
        <p>
            Visit the <strong>Contact Us</strong> page from the main navigation to send feedback, ask questions,
            or report an issue to the OrgaNiss team.
        </p>
    </section>
</div>

